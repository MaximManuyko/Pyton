# Реализуйте функцию merged(), которая объединяет несколько словарей в один общий словарь. Функция принимает список словарей и возвращает результат в виде словаря, в котором каждый ключ содержит множество (set) уникальных значений.

# from solution import merged
# merged([{}, {}]) == {}
# # True
# merged([
#     {'a': 1, 'b': 2},
#     {'b': 10, 'c': 100},
# ]) == {'a': {1}, 'b': {2, 10}, 'c': {100}}

def merged(dicts):
    # Создаем пустой словарь для хранения результата
    result = {}
    
    # Проходим по каждому словарю в списке
    for d in dicts:
        # Проходим по каждой паре ключ-значение в словаре
        for key, value in d.items():
            # Если ключ уже есть в результате, добавляем значение в существующее множество
            if key in result:
                result[key].add(value)
            # В противном случае, создаем новое множество с этим значением
            else:
                result[key] = {value}
    
    return result

# Тестируем функцию
print(merged([{}, {}]))  # {}
print(merged([
    {'a': 1, 'b': 2},
    {'b': 10, 'c': 100},
]))  # {'a': {1}, 'b': {2, 10}, 'c': {100}}
