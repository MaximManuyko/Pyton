def enlarge(image):
    # Создаем пустой список для увеличенного изображения
    enlarged_image = []

    # Перебираем каждую строку изображения
    for line in image:
        # Создаем пустую строку для текущей строки изображения
        enlarged_line = ""
        # Перебираем каждый символ в текущей строке
        for char in line:
            # Удваиваем символ и добавляем к увеличенной строке
            enlarged_line += char * 2
        # Добавляем увеличенную строку дважды к увеличенному изображению
        enlarged_image.append(enlarged_line)
        enlarged_image.append(enlarged_line)

    return enlarged_image

# Функция show() для отображения изображения
def show(image):
    for line in image:
        print(line)

